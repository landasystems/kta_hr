        if ($model->save()) {
//            $delDet = TblDtransPotongan::deleteAll(['no_trans' => $model->no_pot]);
            foreach ($params['detail'] as $key => $val) {
                $detail = TblDtransPotongan::findOne($val['id']);
                $jmlLama = (!empty($detail)) ? $detail->jmlh_brng : 0;
